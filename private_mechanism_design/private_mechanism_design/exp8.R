# ================================================================
# 15. BUILD COMMON REVENUE-TARGET GRIDS
#
# For each epsilon, use the common range
#
#   0 <= R <= min_channel Rmax,
#
# so both channels are plotted over the same feasible targets.
# ================================================================

value_function_list <- list()

value_counter <- 0L


for (epsilon_value in epsilon_grid) {
  rmax_at_epsilon <- rmax_results %>%
    filter(
      abs(
        epsilon -
          epsilon_value
      ) <
        1e-12
    )
  
  common_Rmax <- min(
    rmax_at_epsilon$Rmax
  )
  
  if (
    !is.finite(
      common_Rmax
    ) ||
    common_Rmax <= 0
  ) {
    warning(
      paste0(
        "No common positive revenue range at epsilon = ",
        epsilon_value,
        "."
      )
    )
    
    next
  }
  
  revenue_grid <- seq(
    0,
    0.98 *
      common_Rmax,
    length.out = if (FAST) {
      31
    } else {
      61
    }
  )
  
  for (channel_name in channels) {
    selected_frontier <- empirical_frontiers %>%
      filter(
        channel ==
          channel_name,
        
        abs(
          epsilon -
            epsilon_value
        ) <
          1e-12
      ) %>%
      arrange(
        lambda
      )
    
    value_rows <- evaluate_value_function(
      frontier = selected_frontier,
      revenue_grid = revenue_grid
    )
    
    value_counter <- value_counter +
      1L
    
    value_function_list[[
      value_counter
    ]] <- value_rows %>%
      mutate(
        channel =
          channel_name,
        
        epsilon =
          epsilon_value
      )
  }
}


value_function_results <- bind_rows(
  value_function_list
) %>%
  mutate(
    channel = factor(
      channel,
      levels = channels
    )
  )


write.csv(
  value_function_results,
  file.path(
    data_dir,
    "exp8_welfare_vs_revenue_target.csv"
  ),
  row.names = FALSE
)


