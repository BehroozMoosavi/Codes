channel_log_density <- function(
    signal_value,
    type_grid,
    channel_name,
    epsilon_value
) {
  scale_value <- channel_scale(
    epsilon_value
  )
  
  switch(
    channel_name,
    
    "Laplace" =
      -log(
        2 *
          scale_value
      ) -
      abs(
        signal_value -
          type_grid
      ) /
      scale_value,
    
    "Logistic" =
      dlogis(
        signal_value -
          type_grid,
        location = 0,
        scale = scale_value,
        log = TRUE
      ),
    
    stop(
      paste(
        "Unknown channel:",
        channel_name
      )
    )
  )
}


