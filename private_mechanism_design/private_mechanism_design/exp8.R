x_lower <- 0
x_upper <- 2

support_width <- x_upper - x_lower

beta_shape_1 <- 2
beta_shape_2 <- 2


prior_density <- function(x) {
  u <- (
    x -
      x_lower
  ) /
    support_width
  
  ifelse(
    u >= 0 &
      u <= 1,
    
    dbeta(
      u,
      shape1 = beta_shape_1,
      shape2 = beta_shape_2
    ) /
      support_width,
    
    0
  )
}


prior_cdf <- function(x) {
  u <- (
    x -
      x_lower
  ) /
    support_width
  
  ifelse(
    u <= 0,
    0,
    ifelse(
      u >= 1,
      1,
      pbeta(
        u,
        shape1 = beta_shape_1,
        shape2 = beta_shape_2
      )
    )
  )
}


virtual_value <- function(x) {
  density_value <- prior_density(
    x
  )
  
  distribution_value <- prior_cdf(
    x
  )
  
  x -
    (
      1 -
        distribution_value
    ) /
    pmax(
      density_value,
      1e-12
    )
}


sample_types <- function(number_of_draws) {
  x_lower +
    support_width *
    rbeta(
      number_of_draws,
      shape1 = beta_shape_1,
      shape2 = beta_shape_2
    )
}


