safe_probability <- function(
    probability,
    tolerance = 1e-14
) {
  pmin(
    pmax(
      probability,
      tolerance
    ),
    1 -
      tolerance
  )
}


laplace_quantile <- function(
    probability,
    scale
) {
  probability <- safe_probability(
    probability
  )
  
  ifelse(
    probability < 0.5,
    
    scale *
      log(
        2 *
          probability
      ),
    
    -scale *
      log(
        2 *
          (
            1 -
              probability
          )
      )
  )
}


simulate_channel_from_uniforms <- function(
    type_matrix,
    uniform_matrix,
    channel_name,
    epsilon_value
) {
  scale_value <- channel_scale(
    epsilon_value
  )
  
  uniform_matrix <- safe_probability(
    uniform_matrix
  )
  
  noise_matrix <- switch(
    channel_name,
    
    "Laplace" =
      laplace_quantile(
        probability = uniform_matrix,
        scale = scale_value
      ),
    
    "Logistic" =
      qlogis(
        uniform_matrix,
        location = 0,
        scale = scale_value
      ),
    
    stop(
      paste(
        "Unknown channel:",
        channel_name
      )
    )
  )
  
  type_matrix +
    noise_matrix
}


