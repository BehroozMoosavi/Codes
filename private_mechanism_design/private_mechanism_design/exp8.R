posterior_cache <- new.env(
  parent = emptyenv()
)


posterior_cache_key <- function(
    channel_name,
    epsilon_value
) {
  paste(
    channel_name,
    format(
      epsilon_value,
      scientific = FALSE,
      trim = TRUE
    ),
    sep = "__"
  )
}


get_posterior_functions <- function(
    channel_name,
    epsilon_value
) {
  key <- posterior_cache_key(
    channel_name = channel_name,
    epsilon_value = epsilon_value
  )
  
  if (!exists(
    key,
    envir = posterior_cache,
    inherits = FALSE
  )) {
    cat(
      "Building posterior table:",
      channel_name,
      "| epsilon =",
      epsilon_value,
      "\n"
    )
    
    posterior_table <- posterior_moment_table(
      channel_name = channel_name,
      epsilon_value = epsilon_value
    )
    
    posterior_object <- list(
      xhat = make_interpolator(
        grid = posterior_table$signal_grid,
        values = posterior_table$xhat
      ),
      
      Jhat = make_interpolator(
        grid = posterior_table$signal_grid,
        values = posterior_table$Jhat
      ),
      
      signal_lower = min(
        posterior_table$signal_grid
      ),
      
      signal_upper = max(
        posterior_table$signal_grid
      )
    )
    
    assign(
      key,
      posterior_object,
      envir = posterior_cache
    )
  }
  
  get(
    key,
    envir = posterior_cache,
    inherits = FALSE
  )
}


