# 11. COMPUTE AGGREGATE POSTERIOR STATISTICS
# ================================================================

compute_aggregate_posteriors <- function(
    channel_name,
    epsilon_value
) {
  posterior_functions <- get_posterior_functions(
    channel_name = channel_name,
    epsilon_value = epsilon_value
  )
  
  signal_matrix <- simulate_channel_from_uniforms(
    type_matrix = type_matrix,
    uniform_matrix = uniform_noise_matrix,
    channel_name = channel_name,
    epsilon_value = epsilon_value
  )
  
  signal_vector <- as.vector(
    signal_matrix
  )
  
  posterior_x_vector <- posterior_functions$xhat(
    signal_vector
  )
  
  posterior_J_vector <- posterior_functions$Jhat(
    signal_vector
  )
  
  if (
    any(
      !is.finite(
        posterior_x_vector
      )
    ) ||
    any(
      !is.finite(
        posterior_J_vector
      )
    )
  ) {
    stop(
      paste0(
        "Non-finite interpolated posterior moments for ",
        channel_name,
        " at epsilon = ",
        epsilon_value,
        "."
      )
    )
  }
  
  posterior_x_matrix <- matrix(
    posterior_x_vector,
    nrow = monte_carlo_replications,
    ncol = number_of_agents
  )
  
  posterior_J_matrix <- matrix(
    posterior_J_vector,
    nrow = monte_carlo_replications,
    ncol = number_of_agents
  )
  
  list(
    aggregate_xhat =
      rowSums(
        posterior_x_matrix
      ),
    
    aggregate_Jhat =
      rowSums(
        posterior_J_matrix
      ),
    
    overflow_rate =
      mean(
        signal_vector <
          posterior_functions$signal_lower |
          signal_vector >
          posterior_functions$signal_upper
      )
  )
}


# ================================================================
# 12. EXACT EMPIRICAL WELFARE-REVENUE FRONTIER
#
# At lambda = 0, aggregate_xhat > 0, so every draw is built.
#
# For aggregate_Jhat < 0, draw r stops being built when
#
#   lambda >= -aggregate_xhat_r / aggregate_Jhat_r.
#
# For aggregate_Jhat >= 0, the draw remains built for all lambda.
#
# Sorting the finite switching thresholds gives the exact empirical
# multiplier frontier.
# ================================================================

build_empirical_frontier <- function(
    aggregate_xhat,
    aggregate_Jhat,
    aggregate_true_type,
    first_best_welfare
) {
  number_of_draws <- length(
    aggregate_xhat
  )
  
  if (
    length(
      aggregate_Jhat
    ) !=
    number_of_draws ||
    length(
      aggregate_true_type
    ) !=
    number_of_draws
  ) {
    stop(
      "Aggregate vectors must have equal lengths."
    )
  }
  
  if (
    any(
      aggregate_xhat <= 0
    )
  ) {
    warning(
      paste0(
        "Some aggregate posterior type scores are nonpositive. ",
        "The exact switching simplification may not apply to every draw."
      )
    )
  }
  
  negative_virtual <- aggregate_Jhat < 0
  
  switching_threshold <- rep(
    Inf,
    number_of_draws
  )
  
  switching_threshold[
    negative_virtual
  ] <-
    -aggregate_xhat[
      negative_virtual
    ] /
    aggregate_Jhat[
      negative_virtual
    ]
  
  removable_indices <- which(
    is.finite(
      switching_threshold
    ) &
      switching_threshold >= 0
  )
  
  initial_revenue <- mean(
    aggregate_Jhat
  )
  
  initial_welfare <- mean(
    aggregate_true_type
  )
  
  initial_efficiency <- initial_welfare /
    first_best_welfare
  
  if (length(removable_indices) == 0L) {
    frontier <- data.frame(
      lambda = 0,
      revenue = initial_revenue,
      welfare = initial_welfare,
      efficiency = initial_efficiency,
      build_probability = 1,
      stringsAsFactors = FALSE
    )
    
    return(
      frontier
    )
  }
  
  ordered_indices <- removable_indices[
    order(
      switching_threshold[
        removable_indices
      ],
      method = "radix"
    )
  ]
  
  ordered_thresholds <- switching_threshold[
    ordered_indices
  ]
  
  unique_thresholds <- unique(
    ordered_thresholds
  )
  
  revenue_gain_by_threshold <- tapply(
    -aggregate_Jhat[
      ordered_indices
    ] /
      number_of_draws,
    ordered_thresholds,
    sum
  )
  
  welfare_loss_by_threshold <- tapply(
    aggregate_true_type[
      ordered_indices
    ] /
      number_of_draws,
    ordered_thresholds,
    sum
  )
  
  probability_loss_by_threshold <- tapply(
    rep(
      1 /
        number_of_draws,
      length(
        ordered_indices
      )
    ),
    ordered_thresholds,
    sum
  )
  
  cumulative_revenue_gain <- cumsum(
    as.numeric(
      revenue_gain_by_threshold
    )
  )
  
  cumulative_welfare_loss <- cumsum(
    as.numeric(
      welfare_loss_by_threshold
    )
  )
  
  cumulative_probability_loss <- cumsum(
    as.numeric(
      probability_loss_by_threshold
    )
  )
  
  frontier <- data.frame(
    lambda = c(
      0,
      unique_thresholds
    ),
    
    revenue = c(
      initial_revenue,
      initial_revenue +
        cumulative_revenue_gain
    ),
    
    welfare = c(
      initial_welfare,
      initial_welfare -
        cumulative_welfare_loss
    ),
    
    build_probability = c(
      1,
      1 -
        cumulative_probability_loss
    ),
    
    stringsAsFactors = FALSE
  )
  
  frontier$efficiency <- frontier$welfare /
    first_best_welfare
  
  frontier <- frontier %>%
    arrange(
      lambda
    ) %>%
    mutate(
      revenue =
        cummax(
          revenue
        ),
      
      welfare =
        cummin(
          welfare
        ),
      
      efficiency =
        cummin(
          efficiency
        ),
      
      build_probability =
        cummin(
          build_probability
        )
    )
  
  frontier
}


