# ================================================================
# EXPERIMENT 8
# Maximum welfare as a function of the required revenue target
#
# Optimal Binary Mechanism under Locally Private Signals
#
# PURE LOCAL DIFFERENTIAL PRIVACY ONLY
#
# Channels:
#
#   1. Laplace
#   2. Logistic
#
# No Gaussian or clamped-Gaussian channels are included.
#
# Both channels use the same pure epsilon-LDP calibration:
#
#   scale(epsilon) = Delta / epsilon,
#
# where Delta = upper endpoint - lower endpoint = 2.
#
# PRIOR
#
#   X = 2U,
#   U ~ Beta(2,2),
#   X in [0,2].
#
# The lower endpoint is zero, so this is the revenue knife-edge
# regime. Since X is nonnegative, first-best always builds:
#
#   W_FB = E[sum_i X_i].
#
# VIRTUAL VALUE
#
#   J(x) = x - (1-F(x))/f(x).
#
# POSTERIOR RULE
#
#   q_lambda(Y)
#     =
#     1{
#       sum_i [
#         E[X_i | Y_i]
#         +
#         lambda E[J(X_i) | Y_i]
#       ] >= 0
#     }.
#
# SIGNED EXPECTED REVENUE
#
#   Rev(lambda)
#     =
#     E[
#       (sum_i Jhat_i)
#       q_lambda(Y)
#     ].
#
# WELFARE
#
#   W(lambda)
#     =
#     E[
#       (sum_i X_i)
#       q_lambda(Y)
#     ].
#
# VALUE FUNCTION
#
# For each required target R:
#
#   W*(R)
#     =
#     maximum welfare obtained by the posterior-score rule
#     subject to expected signed revenue being at least R.
#
# EMPIRICAL FRONTIER
#
# The code does not impose monotonicity using cummax().
#
# Since sum_i E[X_i|Y_i] > 0 under this positive-support prior:
#
#   * draws with aggregate Jhat >= 0 are built for every lambda >= 0;
#   * draws with aggregate Jhat < 0 stop being built when
#
#         lambda >= -aggregate_xhat / aggregate_Jhat.
#
# Therefore, every Monte Carlo draw has an exact switching threshold.
# Sorting these thresholds gives the exact empirical welfare-revenue
# frontier generated by the multiplier rule.
#
# NUMERICAL FEATURES
#
#   * common type draws across channels;
#   * common uniform noise draws across channels;
#   * inverse-transform sampling;
#   * adaptive signal grids;
#   * log-stable posterior quadrature;
#   * trapezoidal integration;
#   * vectorized Monte Carlo calculations;
#   * exact empirical switching frontier;
#   * no logarithmic or other axis transformations.
#
# OUTPUT
