# ================================================================

posterior_moment_table <- function(
    channel_name,
    epsilon_value
) {
  type_grid <- seq(
    x_lower,
    x_upper,
    length.out = if (FAST) {
      1201
    } else {
      3001
    }
  )
  
  type_step <- type_grid[2] -
    type_grid[1]
  
  trapezoid_weights <- rep(
    1,
    length(
      type_grid
    )
  )
  
  trapezoid_weights[
    c(
      1,
      length(
        trapezoid_weights
      )
    )
  ] <- 0.5
  
  density_values <- prior_density(
    type_grid
  )
  
  virtual_values <- virtual_value(
    type_grid
  )
  
  signal_grid <- make_signal_grid(
    channel_name = channel_name,
    epsilon_value = epsilon_value
  )
  
  rows <- lapply(
    signal_grid,
    function(signal_value) {
      log_kernel <- channel_log_density(
        signal_value = signal_value,
        type_grid = type_grid,
        channel_name = channel_name,
        epsilon_value = epsilon_value
      )
      
      log_weights <- rep(
        -Inf,
        length(
          type_grid
        )
      )
      
      positive_density <- density_values > 0
      
      log_weights[
        positive_density
      ] <-
        log_kernel[
          positive_density
        ] +
        log(
          density_values[
            positive_density
          ]
        ) +
        log(
          trapezoid_weights[
            positive_density
          ]
        )
      
      maximum_log_weight <- max(
        log_weights
      )
      
      if (!is.finite(maximum_log_weight)) {
        return(
          c(
            xhat = NA_real_,
            Jhat = NA_real_
          )
        )
      }
      
      stabilized_weights <- exp(
        log_weights -
          maximum_log_weight
      )
      
      denominator <- sum(
        stabilized_weights
      ) *
        type_step
      
      if (
        !is.finite(
          denominator
        ) ||
        denominator <=
        .Machine$double.eps
      ) {
        return(
          c(
            xhat = NA_real_,
            Jhat = NA_real_
          )
        )
      }
      
      posterior_x <- sum(
        type_grid *
          stabilized_weights
      ) *
        type_step /
        denominator
      
      posterior_J <- sum(
        virtual_values *
          stabilized_weights
      ) *
        type_step /
        denominator
      
      c(
        xhat = posterior_x,
        Jhat = posterior_J
      )
    }
  )
  
  moment_matrix <- do.call(
    rbind,
    rows
  )
  
  if (
    any(
      !is.finite(
        moment_matrix
      )
    )
  ) {
    stop(
      paste0(
        "Non-finite posterior moments for ",
        channel_name,
        " at epsilon = ",
        epsilon_value,
        "."
      )
    )
  }
  
  list(
    signal_grid =
      signal_grid,
    
    xhat =
      moment_matrix[
        ,
        "xhat"
      ],
    
    Jhat =
      moment_matrix[
        ,
        "Jhat"
      ]
  )
}


make_interpolator <- function(
    grid,
    values
) {
  force(
    grid
  )
  
  force(
    values
  )
  
  function(signal_values) {
    approx(
      x = grid,
      y = values,
      xout = signal_values,
      rule = 2,
      ties = "ordered"
    )$y
  }
}


