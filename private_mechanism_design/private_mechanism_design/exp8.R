# 13. VALUE FUNCTION AT REQUIRED REVENUE TARGETS
#
# For each R, select the first frontier point whose empirical revenue
# is at least R. This ensures the revenue constraint is satisfied.
#
# The value function is stepwise because the empirical distribution
# has finitely many Monte Carlo draws.
# ================================================================

evaluate_value_function <- function(
    frontier,
    revenue_grid
) {
  maximum_revenue <- max(
    frontier$revenue
  )
  
  feasible_targets <- revenue_grid[
    revenue_grid <=
      maximum_revenue +
      1e-12
  ]
  
  if (length(feasible_targets) == 0L) {
    return(
      data.frame()
    )
  }
  
  rows <- lapply(
    feasible_targets,
    function(required_revenue) {
      feasible_index <- which(
        frontier$revenue >=
          required_revenue -
          1e-12
      )[1]
      
      data.frame(
        required_revenue =
          required_revenue,
        
        lambda =
          frontier$lambda[
            feasible_index
          ],
        
        achieved_revenue =
          frontier$revenue[
            feasible_index
          ],
        
        welfare =
          frontier$welfare[
            feasible_index
          ],
        
        efficiency =
          frontier$efficiency[
            feasible_index
          ],
        
        build_probability =
          frontier$build_probability[
            feasible_index
          ],
        
        stringsAsFactors = FALSE
      )
    }
  )
  
  bind_rows(
    rows
  )
}


